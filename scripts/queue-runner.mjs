#!/usr/bin/env node
/**
 * Claude Code OS — Task Queue Runner
 * Runs every 15 minutes via launchd (macOS) or systemd (Linux).
 * Checks _brain/tasks/queue.json for pending tasks and executes them.
 *
 * Option A (preferred): invokes `claude -p` directly — fully autonomous.
 * Fallback: writes task to _brain/inbox/ — Claude picks up at next session.
 */

import { readFile, writeFile, appendFile, mkdir } from 'fs/promises';
import { resolve, dirname } from 'path';
import { fileURLToPath } from 'url';
import { spawnSync, execSync } from 'child_process';
import { existsSync } from 'fs';

const __dirname   = dirname(fileURLToPath(import.meta.url));
const PROJECT_DIR = resolve(__dirname, '../..'); // script lives at _brain/scripts/, project root is two levels up
const QUEUE_PATH  = resolve(PROJECT_DIR, '_brain/tasks/queue.json');
const LOG_PATH    = resolve(PROJECT_DIR, '_brain/scripts/queue-runner.log');

async function log(msg) {
  const line = `[${new Date().toISOString()}] ${msg}\n`;
  process.stdout.write(line);
  try { await appendFile(LOG_PATH, line); } catch {}
}

function findSubRepos(projectDir) {
  try {
    const result = spawnSync(
      'find', ['.', '-name', '.git', '-type', 'd', '-maxdepth', '5'],
      { cwd: projectDir, encoding: 'utf8', stdio: 'pipe' }
    );
    return (result.stdout || '')
      .split('\n')
      .map(l => l.trim())
      .filter(l => l && l !== './.git' && !l.startsWith('./_brain'))
      .map(l => resolve(projectDir, l.replace('/.git', '').slice(2)));
  } catch {
    return [];
  }
}

function isProjectIdle(projectDir) {
  const repos = [projectDir, ...findSubRepos(projectDir)];
  for (const repoDir of repos) {
    try {
      const result = spawnSync('git', ['log', '--since=1 hour ago', '--oneline'], {
        cwd: repoDir, encoding: 'utf8', stdio: 'pipe',
      });
      const humanCommits = (result.stdout || '')
        .split('\n')
        .filter(line => line.trim() && !line.includes('brain:'));
      if (humanCommits.length > 0) return false; // active work detected
    } catch {}
  }
  return true; // all repos idle
}

function findClaude() {
  const candidates = [
    'claude',
    '/usr/local/bin/claude',
    `${process.env.HOME}/.local/bin/claude`,
    '/opt/homebrew/bin/claude',
    `${process.env.HOME}/.npm-global/bin/claude`,
  ];
  for (const bin of candidates) {
    try {
      const result = spawnSync('test', ['-f', bin], { stdio: 'pipe' });
      if (result.status === 0) return bin;
      // also try which
      const which = spawnSync('which', [bin], { stdio: 'pipe' });
      if (which.status === 0) return bin;
    } catch {}
  }
  return null;
}

function getNextRun(schedule) {
  const now  = new Date();
  const days = ['sunday','monday','tuesday','wednesday','thursday','friday','saturday'];

  if (schedule.type === 'interval') {
    const hours = schedule.hours || 6;
    return new Date(now.getTime() + hours * 3600 * 1000).toISOString();
  }

  if (schedule.type === 'weekly') {
    const targetDay = days.indexOf(schedule.day.toLowerCase());
    const [h, m]    = schedule.time.split(':').map(Number);
    const next      = new Date(now);
    next.setHours(h, m, 0, 0);
    let daysUntil   = (targetDay - now.getDay() + 7) % 7;
    if (daysUntil === 0 && next <= now) daysUntil = 7;
    next.setDate(next.getDate() + daysUntil);
    return next.toISOString();
  }

  if (schedule.type === 'daily') {
    const [h, m] = schedule.time.split(':').map(Number);
    const next   = new Date(now);
    next.setHours(h, m, 0, 0);
    if (next <= now) next.setDate(next.getDate() + 1);
    return next.toISOString();
  }

  return null;
}

async function runWithClaude(claudeBin, task) {
  await log(`Invoking claude for: ${task.name}`);
  const result = spawnSync(claudeBin, ['-p', task.prompt], {
    cwd:      PROJECT_DIR,
    encoding: 'utf8',
    timeout:  300_000, // 5 minutes
    env:      process.env,
  });

  if (result.status === 0) {
    await log(`Done: ${task.name}`);
    if (result.stdout) await log(`Output: ${result.stdout.slice(0, 500)}`);
    return true;
  } else {
    await log(`Failed (exit ${result.status}): ${result.stderr?.slice(0, 300)}`);
    return false;
  }
}

async function runViaInbox(task) {
  // Fallback: drop task into inbox so Claude handles it at next session start
  const date     = new Date().toISOString().split('T')[0];
  const inboxDir = resolve(PROJECT_DIR, '_brain/inbox');
  await mkdir(inboxDir, { recursive: true });

  const filePath = resolve(inboxDir, `task-${task.id}-${date}.md`);
  const content  = [
    `# Pending Task: ${task.name}`,
    ``,
    `> This task was scheduled for ${task.next_run} but \`claude\` CLI was not found.`,
    `> Process this now.`,
    ``,
    task.prompt,
    ``,
    `_Auto-generated by queue-runner — ${new Date().toISOString()}_`,
  ].join('\n');

  await writeFile(filePath, content);
  await log(`Queued to inbox (claude not found): ${filePath}`);
  return true;
}

async function updateDashboardScheduledTasks(queue) {
  const dashboardPath = resolve(PROJECT_DIR, '_brain/dashboard.html');
  if (!existsSync(dashboardPath)) return;

  try {
    let html = await readFile(dashboardPath, 'utf8');

    const fmt = iso => iso
      ? new Date(iso).toLocaleString(undefined, { month: 'short', day: 'numeric', hour: '2-digit', minute: '2-digit' })
      : '—';

    const rows = queue.tasks.map(task => {
      const dot   = task.enabled ? (task.status === 'running' ? 'bg-amber-400' : 'bg-emerald-500') : 'bg-gray-300 dark:bg-gray-600';
      const badge = task.enabled ? task.status : 'disabled';
      return `        <div class="flex items-start justify-between gap-4 py-3 border-b border-gray-100 dark:border-gray-800 last:border-0">
          <div class="flex items-start gap-3 flex-1 min-w-0">
            <span class="mt-1.5 w-2 h-2 rounded-full ${dot} flex-shrink-0"></span>
            <div class="flex-1 min-w-0">
              <div class="flex items-center gap-2 flex-wrap">
                <span class="text-sm font-medium">${task.name}</span>
                <span class="text-xs px-2 py-0.5 rounded-full bg-gray-100 dark:bg-gray-800 text-gray-500">${badge}</span>
              </div>
              <div class="text-xs text-gray-400 dark:text-gray-500 mt-0.5">${task.description}</div>
            </div>
          </div>
          <div class="text-right flex-shrink-0 text-xs text-gray-400 dark:text-gray-500 space-y-0.5">
            <div>Last: ${fmt(task.last_run)}</div>
            <div>Next: ${fmt(task.next_run)}</div>
          </div>
        </div>`;
    }).join('\n');

    const block = `\n      <div class="divide-y divide-transparent">\n${rows}\n      </div>\n    `;
    html = html.replace(
      /<!-- SCHEDULED_START -->[\s\S]*?<!-- SCHEDULED_END -->/,
      `<!-- SCHEDULED_START -->${block}<!-- SCHEDULED_END -->`
    );

    await writeFile(dashboardPath, html);
  } catch (err) {
    await log(`Dashboard scheduled tasks update failed: ${err.message}`);
  }
}

function gitCommit(taskName) {
  try {
    execSync(`git add _brain/ && git commit -m "brain: auto-task — ${taskName}"`, {
      cwd:   PROJECT_DIR,
      stdio: 'pipe',
    });
  } catch {}
}

async function main() {
  await log('Queue runner started');

  if (!existsSync(QUEUE_PATH)) {
    await log('No queue.json — nothing to do');
    return;
  }

  const queue    = JSON.parse(await readFile(QUEUE_PATH, 'utf8'));
  const now      = new Date();
  const claudeBin = findClaude();

  if (!claudeBin) {
    await log('claude CLI not found — will use inbox fallback for pending tasks');
  }

  let changed = false;

  for (const task of queue.tasks) {
    if (!task.enabled || task.status === 'running') continue;
    if (!task.next_run) continue;

    const nextRun = new Date(task.next_run);
    if (nextRun > now) {
      await log(`${task.name}: next run at ${task.next_run} — skipping`);
      continue;
    }

    // Proactive check only runs when project is idle (no human commits in last hour)
    if (task.id === 'proactive-check' && !isProjectIdle(PROJECT_DIR)) {
      await log(`${task.name}: project active (recent commits) — deferring to next cycle`);
      continue;
    }

    await log(`Executing: ${task.name} (overdue since ${task.next_run})`);
    task.status = 'running';
    changed     = true;
    await writeFile(QUEUE_PATH, JSON.stringify(queue, null, 2));

    const success = claudeBin
      ? await runWithClaude(claudeBin, task)
      : await runViaInbox(task);

    task.last_run = now.toISOString();
    task.next_run = getNextRun(task.schedule);
    task.status   = 'pending';
    changed       = true;

    if (success) gitCommit(task.name);
  }

  if (changed) {
    await writeFile(QUEUE_PATH, JSON.stringify(queue, null, 2));
    await updateDashboardScheduledTasks(queue);
  }

  await log('Queue runner finished');
}

main().catch(async err => {
  await log(`Fatal: ${err.message}`);
  process.exit(1);
});
